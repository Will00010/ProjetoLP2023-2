    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed

        if (jTxtInteresses.getText().equals("") && jCboEC.getSelectedIndex() == 0) {
            List lista = clientesDAO.listAll();
            clientesControle.setList(lista);
        } else {
            if (!jTxtInteresses.getText().equals("") && jCboEC.getSelectedIndex() == 1) {
                List lista = clientesDAO.listInteressesEC(jTxtInteresses.getText(), 0);
                clientesControle.setList(lista);
            }
            if (!jTxtInteresses.getText().equals("") && jCboEC.getSelectedIndex() == 2) {
                List lista = clientesDAO.listInteressesEC(jTxtInteresses.getText(), 1);
                clientesControle.setList(lista);
            }
            if (!jTxtInteresses.getText().equals("") && jCboEC.getSelectedIndex() == 3) {
                List lista = clientesDAO.listInteressesEC(jTxtInteresses.getText(), 2);
                clientesControle.setList(lista);
            }
            if (!jTxtInteresses.getText().equals("") && jCboEC.getSelectedIndex() == 4) {
                List lista = clientesDAO.listInteressesEC(jTxtInteresses.getText(), 3);
                clientesControle.setList(lista);
            } else {
                if (!jTxtInteresses.getText().equals("")) {
                    List lista = clientesDAO.listInteresses(jTxtInteresses.getText());
                    clientesControle.setList(lista);
                } else {
                    if (jCboEC.getSelectedIndex() == 1) {
                        List lista = clientesDAO.listEC(0);
                        clientesControle.setList(lista);
                    }
                    if (jCboEC.getSelectedIndex() == 2) {
                        List lista = clientesDAO.listEC(1);
                        clientesControle.setList(lista);
                    }
                    if (jCboEC.getSelectedIndex() == 3) {
                        List lista = clientesDAO.listEC(2);
                        clientesControle.setList(lista);
                    }
                    if (jCboEC.getSelectedIndex() == 4) {
                        List lista = clientesDAO.listEC(3);
                        clientesControle.setList(lista);
                    }
                }
            }
        }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

